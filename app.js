/* ==========================================================================
   3D Tilt Card Animation (Organic Hover Effect)
   ========================================================================== */
function init3DTilt() {
  projectCards.forEach(card => {
    let request = null;
    
    function handleMove(e) {
      const rect = card.getBoundingClientRect();
      const x = e.clientX - rect.left;
      const y = e.clientY - rect.top;
      
      const centerX = rect.width / 2;
      const centerY = rect.height / 2;
      
      // Calculate rotation angles (max 6 degrees tilt)
      const rotateX = -(y - centerY) / (centerY / 6);
      const rotateY = (x - centerX) / (centerX / 6);
      
      if (request) cancelAnimationFrame(request);
      request = requestAnimationFrame(() => {
        card.style.transform = `perspective(1000px) rotateX(${rotateX}deg) rotateY(${rotateY}deg) scale3d(1.02, 1.02, 1.02)`;
        card.style.transition = "transform 0.1s cubic-bezier(0.25, 1, 0.5, 1)";
      });
    }
    
    function handleLeave() {
      if (request) cancelAnimationFrame(request);
      card.style.transform = "perspective(1000px) rotateX(0deg) rotateY(0deg) scale3d(1, 1, 1)";
      card.style.transition = "transform 0.5s cubic-bezier(0.25, 1, 0.5, 1)";
    }
    
    card.addEventListener("mousemove", handleMove);
    card.addEventListener("mouseleave", handleLeave);
  });
}

/* ==========================================================================
   Intersection Observer (Scroll Reveal Animations)
   ========================================================================== */
const revealElements = document.querySelectorAll(".reveal, .timeline-item, .project-card, .skills-category");

const revealObserver = new IntersectionObserver((entries, observer) => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      entry.target.classList.add("active");
      
      // If skills block enters view, trigger individual skill animations
      if (entry.target.classList.contains("skills-category")) {
        entry.target.classList.add("animate-skills");
      }
      
      observer.unobserve(entry.target);
    }
  });
}, {
  threshold: 0.15,
  rootMargin: "0px 0px -50px 0px"
});

// Prepare elements for reveal
revealElements.forEach(el => {
  el.classList.add("reveal");
  revealObserver.observe(el);
});

// Dynamic sticky header highlight on scroll
window.addEventListener("scroll", () => {
  const header = document.querySelector(".header");
  if (header) {
    if (window.scrollY > 50) {
      header.classList.add("blur-header");
    } else {
      header.classList.remove("blur-header");
    }
  }

  // Active section nav link tracking
  const sections = document.querySelectorAll("section");
  let currentSecId = "";
  
  sections.forEach(sec => {
    const secTop = sec.offsetTop - 120;
    const secHeight = sec.clientHeight;
    if (window.scrollY >= secTop && window.scrollY < secTop + secHeight) {
      currentSecId = sec.getAttribute("id");
    }
  });

  navLinks.forEach(link => {
    link.classList.remove("active");
    if (link.getAttribute("href") === `#${currentSecId}`) {
      link.classList.add("active");
    }
  });
});

