/* ==========================================================================
   State Management & Language Selector
   ========================================================================== */
let currentLang = localStorage.getItem("lang") || "fr";

function initLanguage() {
  document.documentElement.lang = currentLang;
  
  // Update translation tags
  const elements = document.querySelectorAll("[data-i18n]");
  elements.forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (i18n[currentLang] && i18n[currentLang][key]) {
      // If it contains inner HTML tags (like strong tags in bio), preserve them
      if (el.innerHTML.includes("<strong") || el.innerHTML.includes("<em")) {
        el.innerHTML = i18n[currentLang][key];
      } else {
        el.textContent = i18n[currentLang][key];
      }
    }
  });

  // Update SEO Page metadata
  document.title = i18n[currentLang]["meta-title"];
  const metaDescEl = document.getElementById("meta-desc");
  if (metaDescEl) {
    metaDescEl.setAttribute("content", i18n[currentLang]["meta-desc"]);
  }

  // Update form placeholders
  const nameInput = document.getElementById("form-name");
  const emailInput = document.getElementById("form-email");
  const msgInput = document.getElementById("form-msg");
  if (nameInput) nameInput.placeholder = currentLang === "fr" ? "Quentin" : "John Doe";
  if (emailInput) emailInput.placeholder = currentLang === "fr" ? "nom@exemple.com" : "john@example.com";
  if (msgInput) msgInput.placeholder = currentLang === "fr" ? "Votre message..." : "Your message here...";

  // Update language button text
  const langText = document.querySelector(".lang-text");
  if (langText) {
    langText.textContent = currentLang === "fr" ? "EN" : "FR";
  }
}

